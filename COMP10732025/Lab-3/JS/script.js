// created a constructor function for toycars
function ToyCar(brand, model, color, scale, price, wheels, material, isElectric, year, features) {
    this.price = price;  
    this.color = color;           
    this.scale = scale;                    
    this.wheels = wheels;         
    this.material = material; 
    this.brand = brand;           
    this.model = model;               
    this.isElectric = isElectric;
    this.year = year;             
    this.features = features;
    
}